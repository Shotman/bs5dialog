/**
 * Creates an icon element with the given name, class, and style.
 * @param {string} iconName - The name of the icon.
 * @param {string} iconClass - The class of the icon.
 * @param {string} iconStyle - The style of the icon.
 * @returns {HTMLElement} - The icon element.
 */
export function makeIcon(iconName = "", iconClass = "", iconStyle = "") {
  iconName = iconName.replace(/\s+/g, " ");
  iconClass = iconClass.replace(/\s+/g, " ");
  iconStyle = iconStyle.replace(/\s+/g, " ");
  // create a new icon element
  let iconElement = document.createElement("i");
  if (!iconName) {
    return iconElement;
  }

  // check if the icon is in the default icon map
  const defaultIcon = BS5_DIALOG_DEFAULT_ICONS[iconName];
  if (defaultIcon) {
    // check if the default icon is an SVG string
    if (defaultIcon.startsWith("<svg")) {
      iconElement.innerHTML = defaultIcon;
      iconElement = iconElement.firstChild;
    } else {
      iconElement.classList.add(...defaultIcon.trim().split(" "));
    }
  } else {
    // assume the icon is a class name or SVG string
    if (iconName.startsWith("<svg")) {
      iconElement.innerHTML = iconName;
      iconElement = iconElement.firstChild;
    } else {
      if (iconName.trim()) {
        iconElement.classList.add(...iconName.trim().split(" "));
      }
    }
  }

  // add iconClass to the icon element
  if (iconClass.trim()) {
    iconElement.classList.add(...iconClass.trim().split(" "));
  }

  // add custom styles to the icon element
  if (iconStyle.trim()) {
    const styles = iconStyle.split(";");
    styles.forEach(style => {
      const [property, value] = style.split(":");
      if (property && value) {
        iconElement.style.setProperty(property.trim(), value.trim());
      }
    });
  }

  return iconElement;
}
